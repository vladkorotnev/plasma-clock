const melody_sequence_t * melody_from_no(int melody_no) {
    int no = std::min(melody_no, all_chime_count);
    no  = std::max(no, 0);
    if(no == all_chime_count) {
        no = esp_random() % all_chime_count;
    }
    
    return &all_chime_list[no];
}

